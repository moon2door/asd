    public static Color GetTable(float value, float min, float max, ColorTables table = ColorTables.TableViridis)
    {
        float v = value;
        if (v < min) v = min;
        if (max < v) v = max;

        uint idx = (uint)Mathf.FloorToInt(255 * (v - min) / (max - min));

        Color color = Color.white;
        switch (table)
        {
            case ColorTables.TableBlueOrange:
                color = tableBlueOrange[idx];
                break;
            case ColorTables.TableFlare:
                color = tableFlare[idx];
                break;
            case ColorTables.TableNeonGreen:
                color = tableNeonGreen[idx];
                break;
            case ColorTables.TableRgb:
                color = tableRgb[idx];
                break;
            case ColorTables.TableRoyal:
                color = tableRoyal[idx];
                break;
            case ColorTables.TableSiemens:
                color = tableSiemens[idx];
                break;
            case ColorTables.TableViridis:
                color = tableViridis[idx];
                break;
        }
        return color;
    }

